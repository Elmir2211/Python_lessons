# Задача-1:
# Дан список, заполненный произвольными целыми числами, получите новый список,
# элементами которого будут квадратные корни элементов исходного списка,
# но только если результаты извлечения корня не имеют десятичной части и
# если такой корень вообще можно извлечь
# Пример: Дано: [2, -5, 8, 9, -25, 25, 4]   Результат: [3, 5, 2]

import math
lst=[]
new_lst=[]
print('Решение 1-й задачи: ')
print('Введите 10 произвольных целых чисел из промежутка от -1000 до 1000: ')
for i in range(10):
    lst.append(int(input('Введите '+str(i+1)+' число: ')))
print('Список с произвольными числами: ', lst)    
print('\nПолучим новый список, элементами которого будут квадратные корни элементов исходного списка, но только если результаты извлечения корня не имеют десятичной части и если такой корень вообще можно извлечь ')        
for s in lst:
    if (s>0):
        q=math.sqrt(s)
        if (q%int(q))==0:
            new_lst.append(int(q))
print('\nНовый список: ',new_lst)

