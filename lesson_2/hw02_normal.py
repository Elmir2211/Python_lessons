# Задача-4: Дан список, заполненный произвольными целыми числами.
# Получите новый список, элементами которого будут: 
# а) неповторяющиеся элементы исходного списка:
# например, lst = [1, 2, 4, 5, 6, 2, 5, 2], нужно получить lst2 = [1, 2, 4, 5, 6]
# б) элементы исходного списка, которые не имеют повторений:
# например, lst = [1 , 2, 4, 5, 6, 2, 5, 2], нужно получить lst2 = [1, 4, 6]

print('Решение 4-й задачи: ')
spk=[1,2,4,5,6,2,5,2]
print('Дан список: ',spk)
spk1=[]
for i in spk:
    if spk.count(i)>1:
        spk1.append('')
    else:
        spk1.append(i)
    if '' in spk1:
        spk1.pop(spk1.index(''))
print('Список, элементами которого будут элементы исходного списка, которые не имеют повторений: ',spk1)
for j in spk:
    p=spk.count(j)
    while p>1:
        k=spk.index(j)
        spk[k]=''
        p=p-1
    if '' in spk:
        spk.pop(spk.index(''))
print('Список, элементами которого будут неповторяющиеся элементы исходного списка: ',spk)

